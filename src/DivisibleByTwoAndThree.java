public class DivisibleByTwoAndThree {
    public static void main(String[] args) {
        int num = 6;
        if (num % 2 == 0 && num % 3 == 0) {
            System.out.println("Number Divisible By Two and Three");
        } else {
            System.out.println("Number Not Divisible By Two and Three");
        }
    }
}
